class Calculator:
   def multiply(self, x, y):
       """Умножение"""
       return x * y

   def division(self, x, y):
       """Деление"""
       return x / y

   def substraction(self, x, y):
       """Разность"""
       return x - y

   def adding(self, x, y):
       """Сложение"""
       return x + y
   